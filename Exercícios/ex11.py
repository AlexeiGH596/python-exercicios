largura = float(input('Digite a largura da parede em metros: '))
altura = float(input('Digite a altura da parede em metros: '))
área = largura*altura
print(f'A dimensão da parede é de {largura}x{altura}\nLogo, a área dela é de {área}m²')
print(f'Você precisará de {área/2}l de tinta para pintar a parede.')
